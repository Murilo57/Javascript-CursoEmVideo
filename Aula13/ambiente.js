//Estrutura de repetição com teste logico no inicio
var c = 1
while (c <= 6) {
    console.log(`Passo ${c}`)
    c++
}

//Estrutura de repetição com teste logico no final
do{
   console.log(`Passo ${c}`)
   c++
} while(c <= 6)

